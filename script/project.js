/* =========================
   TECH LABELS
========================= */
function renderTechs(techs = []) {
  return techs
    .map(tech => `<span class="tech-badge">${tech}</span>`)
    .join("");
}

/* =========================
   RENDER CARDS
========================= */
function renderProjects(list, containerId) {
  const container = document.getElementById(containerId);
  container.innerHTML = "";

  list.forEach(project => {
    const card = document.createElement("article");
    card.className = "project-card";

    card.innerHTML = `
      <img 
        src="${project.cover || project.images[0]}" 
        alt="${project.title}" 
        class="project-thumb"
      >

      <div class="project-card__body">
        <h4>${project.title}</h4>
        <p>${project.description}</p>

        <div class="project-techs">
          ${renderTechs(project.technologies)}
        </div>

        <div class="project-actions">
          <button class="project-btn">Ver detalhes</button>

          <a 
            class="project-link" 
            href="${project.github}" 
            target="_blank"
          >
            GitHub
          </a>
        </div>
      </div>
    `;

    card.querySelector(".project-btn").addEventListener("click", e => {
      e.stopPropagation();
      openProject(project);
    });

    container.appendChild(card);
  });
}

/* =========================
   MODAL
========================= */
function openProject(project) {
  const details = document.getElementById("projectDetails");

  const isLocalVideo = project.video.endsWith(".mp4");

  details.innerHTML = `
    <div class="project-details">
      <h2>${project.title}</h2>

      <p>${project.description}</p>

      <div class="project-techs">
        ${renderTechs(project.technologies)}
      </div>

      ${isLocalVideo
      ? `
          <video class="project-video" controls>
            <source src="${project.video}" type="video/mp4">
          </video>
        `
      : `
          <iframe
            class="project-video"
            src="${project.video}"
            frameborder="0"
            allowfullscreen>
          </iframe>
        `
    }

      <div class="project-info-box">
        <h3>Sobre o projeto</h3>
        <p>${project.fullDescription}</p>
      </div>

      <div class="project-gallery">
        ${project.images
      .map(
        img => `
            <img src="${img}" alt="${project.title}">
          `
      )
      .join("")}
      </div>

      <div class="project-actions" style="margin-top:2rem;">
        <a class="project-link" href="${project.github}" target="_blank">
          Ver GitHub
        </a>
      </div>
    </div>
  `;

  document.getElementById("projectModal").classList.add("active");
}

